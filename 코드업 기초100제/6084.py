# 입력
# h, b, c, s 가 공백을 두고 입력된다.
# h는 48,000이하, b는 32이하(단, 8의배수), c는 5이하, s는 6,000이하의 자연수이다.

# 출력
# 필요한 저장 공간을 MB 단위로 바꾸어 출력한다.
# 단, 소수점 첫째 자리까지의 정확도로 출력하고 MB를 공백을 두고 출력한다.

# h: 1초동안 마이크로 소리강약 체크하는 횟수
# b: 한 번 체크한 값을 저장할 때 사용하는 비트 수 (2비트 0또는 1, 16비트 65536가지)
# c: 좌우 등 소리를 저장할 트랙 개수 (모노:1개 스테레오:2개)

h,b,c,s=input().split()
h,b,c,s=int(h),int(b),int(c),int(s)

st=(h*b*c*s)/8/1024/1024
print('{0:.1f} MB'.format(st))